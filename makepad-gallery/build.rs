use std::ffi::OsStr;
use std::fs;
use std::io::Write;
use std::path::{Path, PathBuf};

const GENERATED_DATA_FILENAME: &str = "icon_gallery_data.rs";

fn to_widget_name(icon_stem: &str) -> Option<String> {
    let mut widget = String::from("Icon");
    let mut has_part = false;

    for part in icon_stem
        .split(|c: char| !c.is_ascii_alphanumeric())
        .filter(|part| !part.is_empty())
    {
        has_part = true;
        let mut chars = part.chars();
        if let Some(first) = chars.next() {
            widget.push(first.to_ascii_uppercase());
            widget.extend(chars);
        }
    }

    has_part.then_some(widget)
}

/// Returns generated icon entries as `(widget_name, icon_stem, absolute_path)` tuples.
fn icon_entries(icons_dir: &Path) -> std::io::Result<Vec<(String, String, PathBuf)>> {
    let mut paths: Vec<PathBuf> = fs::read_dir(icons_dir)?
        .filter_map(|entry| entry.ok().map(|value| value.path()))
        .filter(|path| path.extension().is_some_and(|ext| ext == OsStr::new("svg")))
        .collect();
    paths.sort();

    let mut entries = Vec::with_capacity(paths.len());
    for path in paths {
        println!("cargo:rerun-if-changed={}", path.display());

        let stem = path
            .file_stem()
            .and_then(OsStr::to_str)
            .unwrap_or_else(|| panic!("icon file stem must be valid UTF-8: {}", path.display()));

        if let Some(widget_name) = to_widget_name(stem) {
            entries.push((widget_name, stem.to_string(), path));
        }
    }
    Ok(entries)
}

fn generate_data(entries: &[(String, String, PathBuf)]) -> String {
    let mut output = String::new();
    output.push_str("// Generated by makepad-gallery/build.rs. Do not edit manually.\n");
    output.push_str(&format!(
        "const ICON_GALLERY_TOTAL: usize = {};\n",
        entries.len()
    ));
    output.push_str("const ICON_GALLERY_ENTRIES: &[IconGalleryEntry] = &[\n");
    for (widget_name, label, path) in entries {
        output.push_str(&format!(
            "    IconGalleryEntry{{icon_name: {label:?}, widget_name: {widget_name:?}, svg_markup: include_str!({path:?})}},\n"
        ));
    }
    output.push_str("];\n");
    output
}

fn main() {
    let manifest_dir =
        PathBuf::from(std::env::var("CARGO_MANIFEST_DIR").expect("CARGO_MANIFEST_DIR not set"));
    let icons_dir = manifest_dir
        .parent()
        .expect("makepad-gallery should have workspace root parent")
        .join("makepad-icon")
        .join("resources")
        .join("icons");
    println!("cargo:rerun-if-changed={}", icons_dir.display());

    let entries = icon_entries(&icons_dir).expect("failed to read icon resources");
    let generated = generate_data(&entries);
    let out_path = PathBuf::from(std::env::var("OUT_DIR").expect("OUT_DIR not set"))
        .join(GENERATED_DATA_FILENAME);
    let mut file = fs::File::create(&out_path).unwrap_or_else(|error| {
        panic!(
            "failed to create generated icon data at {}: {error}",
            out_path.display()
        )
    });
    file.write_all(generated.as_bytes())
        .unwrap_or_else(|error| {
            panic!(
                "failed to write generated icon data at {}: {error}",
                out_path.display()
            )
        });
}
